package Alumnes;

public class Loteria {
	
	String nom;
	int numLot;
	int numSel;
	
	
	Loteria(){
		
	}


	public String getNom() {
		return nom;
	}


	public void setNom(String nom) {
		this.nom = nom;
	}


	public int getNumLot() {
		return numLot;
	}


	public void setNumLot(int numLot) {
		this.numLot = numLot;
	}


	public int getNumSel() {
		return numSel;
	}


	public void setNumSel(int numSel) {
		this.numSel = numSel;
	}
	
}
